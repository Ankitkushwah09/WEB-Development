/*

Conditional statements in JavaScript, like in many other programming languages, are
used to make decisions based on certain conditions. They allow the program to execute 
different blocks of code depending on whether a specific condition is true or false.

The most common conditional statement in JavaScript is the if-else statement. 
Its syntax is as follows:

if (condition) {
  // code to be executed if condition is true
 } else {
  // code to be executed if condition is false
}

For example, let's consider a program that checks if a given number is positive 
or negative:

*/

let num = 10;

if (num > 0) {
  console.log("The number is positive.");
} else {
  console.log("The number is negative.");
}